SELECT
	MGY.GYOUSYA_CODE,
	MGY.GYOUSYA_NAME,
	MGY.GYOUSYA_KANA,
	MGE.TORIHIKI_STATUS
FROM
	M_GYOUSYA_EIGYOUSYO MGE
LEFT OUTER JOIN M_GYOUSYA MGY ON MGE.GYOUSYA_CODE = MGY.GYOUSYA_CODE
WHERE
	MGY.GYOUSYA_CODE LIKE 'A%' AND
	MGE.EIGYOUSYO_CODE = /*eigyousyoCode*/0
	/*%if torihikiStatus == "0"*/
	AND MGE.TORIHIKI_STATUS = '0'
	/*%elseif torihikiStatus == "1"*/
	AND MGE.TORIHIKI_STATUS = '1'
	/*%elseif torihikiStatus == "2"*/
	/*%end*/
ORDER BY
	MGY.GYOUSYA_KANA COLLATE utf8_unicode_ci ASC;