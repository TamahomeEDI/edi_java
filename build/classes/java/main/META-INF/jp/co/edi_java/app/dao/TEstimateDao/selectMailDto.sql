SELECT
	EST.ESTIMATE_NUMBER,
	EST.PARTNER_BIKOU,
	EIG.EIGYOUSYO_NAME,
	SYA.SYAIN_NAME,
	KOU.KOUJI_NAME,
	KOU.EIGYOUSYO_CODE,
	GYO.GYOUSYA_NAME
FROM
	T_ESTIMATE EST
LEFT OUTER JOIN T_ESTIMATE_REQUEST ESR ON EST.ESTIMATE_REQUEST_NUMBER = ESR.ESTIMATE_REQUEST_NUMBER
LEFT OUTER JOIN M_KOUJI KOU ON EST.KOUJI_CODE = KOU.KOUJI_CODE
LEFT OUTER JOIN M_SYAIN SYA ON ESR.REGIST_SYAIN_CODE = SYA.SYAIN_CODE
LEFT OUTER JOIN M_GYOUSYA GYO ON EST.GYOUSYA_CODE = GYO.GYOUSYA_CODE
LEFT OUTER JOIN M_EIGYOUSYO EIG ON KOU.EIGYOUSYO_CODE = EIG.EIGYOUSYO_CODE
WHERE
	EST.ESTIMATE_NUMBER = /*estimateNumber*/0;
